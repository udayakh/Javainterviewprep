public class A1 {
    public static void main(String[] args) {
        int c=2;
        int n=10;
        for (int i = 0; i <= n; i=i+c) {
            System.out.println(i);
        }

        System.out.println("===");
        for (int i = n; i >= 0; i=i-c) {
            System.out.println(i);
        }




    }
}
